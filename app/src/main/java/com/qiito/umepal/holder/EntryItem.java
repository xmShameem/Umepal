package com.qiito.umepal.holder;

public class EntryItem {

	public final int image;
	public final String title;

	public EntryItem(int image, String title) {
		this.image = image;
		this.title = title;
	}

	public int getImage() {
		return image;
	}

	public String getTitle() {
		return title;
	}

	

}